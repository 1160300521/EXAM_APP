package P3.graph;

/**
 * the interface to stand for the stop
 * 
 */
public interface Stop {

	/**
	 * get the stop name
	 * 
	 * @return name the name of the stop
	 */
	String getName();

	/**
	 * get the latitude value of the stop
	 * 
	 * @return latitude the latitude of the stop
	 */
	double getLatitude();

	/**
	 * get the longitude value of the stop
	 * 
	 * @return longitude the longitude of the stop
	 */
	double getLongitude();

}
